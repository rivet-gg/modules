import { dedent } from "./deps.ts";

export function autoGenHeader(commentChar = "//") {
	return dedent`
		${commentChar} This file is auto-generated by the Open Game Backend (https://opengb.dev) build system.
		${commentChar} 
		${commentChar} Do not edit this file directly.
		${commentChar}
		${commentChar} Generated at ${new Date().toISOString()}

	`;
}
